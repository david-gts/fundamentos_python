#Calcular el área de un triángulo, se debe ingresar como datos la base y la altura del triángulo.
#A=(base x altura)/2
#Ejercicio03

print("======================================")
print("     CÁLCULO DEL ÁREA DE UN TRIÁNGULO")
print("======================================")
base = float(input("Ingrese la longitud de la base del triángulo: "))
altura = float(input("Ingrese la altura del triángulo: "))
area = (base * altura) / 2
print("--------------------------------------")
print("El área del triángulo es: ", area, "cm²")
print("======================================")

